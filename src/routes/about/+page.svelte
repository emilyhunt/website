<h1>About me</h1>

<p>
	I grew up in Coventry in the UK, and did my undergraduate degree in the UK at the University of
	Bath. In 2019, I moved to Heidelberg in Germany to do my PhD finding open clusters in data from
	the Gaia satellite. It was quite the adventure through the pandemic, but living abroad is tons of
	fun.
</p>

<p>
	I am fiercely passionate about improving inclusivity & equality in science for all. To quote the
	charity Pride in STEM, “Science does not happen in a vacuum.” The environment in which we conduct
	research can have a profound effect on the wellbeing of minority groups. It is essential that
	science is a welcoming place, so that all minds can flourish and achieve their best.
</p>

